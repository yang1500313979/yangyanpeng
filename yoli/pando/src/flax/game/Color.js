function hexToRgb(hex)
{
    var h = (hex.charAt(0)=="#") ? hex.substring(1,7) : hex;
    var r = parseInt(h.substring(0,2),16);
    var g = parseInt(h.substring(2,4),16);
    var b = parseInt(h.substring(4,6),16);
    return [r, g, b];
}

